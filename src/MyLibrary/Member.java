        public void member_load()
        {
            int c;
        try {
            pst=con.prepareStatement("select * from member");//member is the table name in database
            rs=pst.executeQuery();
            ResultSetMetaData rsd=rs.getMetaData();
            c=rsd.getColumnCount();
            
            DefaultTableModel d=(DefaultTableModel)jTable1.getModel();//jTable1 is the name of the table variable
            d.setRowCount(0);
            
            while(rs.next())
            {
            Vector v2=new Vector();
                for(int i=1;i<=c;i++)
                {
                v2.add(rs.getString("id"));
                v2.add(rs.getString("member_name")); // these will be names of the columns of database table
                v2.add(rs.getString("member_address"));
                v2.add(rs.getString("member_phone"));
                }
            d.addRow(v2);
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(Member.class.getName()).log(Level.SEVERE, null, ex);
        }
            
        
        
        
        
        }
        
        
    
