    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
            //action of Update button-sending Updated member details to sql and jtable
            
            DefaultTableModel d1=(DefaultTableModel)jTable1.getModel();
            int selectIndex=jTable1.getSelectedRow(); //row no of the selected data from the jtable is being fetched
            
            int id=Integer.parseInt(d1.getValueAt(selectIndex,0).toString());
            
        String name = txtname.getText();
        String address = txtaddress.getText();
        String phone= txtphone.getText();
        try {
            pst = con.prepareStatement("update member set member_name = ?, member_address= ?, member_phone= ? where id = ?"); //1st one is database table name,rest are column name
            pst.setString(1, name);
            pst.setString(2,address);
            pst.setString(3,phone);
            pst.setInt(4, id);
            int k = pst.executeUpdate();
            if(k==1)
            {
            JOptionPane.showMessageDialog(this, "Member details Updated successfully..");
            txtname.setText("");//to clear the name box
            txtaddress.setText("");//to clear the address box
            txtphone.setText("");//to clear the phone box
            txtname.requestFocus();//to bring the cursor on name box
            member_load();//load the updated data on the jtable
            jButton1.setEnabled(true);//reenable the Add button
            }
            else
            {
            JOptionPane.showMessageDialog(this, "Error in Member details Updatation!!");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Member.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

